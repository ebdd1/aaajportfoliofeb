<script setup>
import { computed } from 'vue';

const emit = defineEmits(['update:checked']);

const props = defineProps({
    checked: {
        type: [Array, Boolean],
        required: true,
    },
    value: {
        default: null,
    },
});

const proxyChecked = computed({
    get() {
        return props.checked;
    },
    set(val) {
        emit('update:checked', val);
    },
});
</script>

<template>
    <input
        type="checkbox"
        :value="value"
        v-model="proxyChecked"
        class="w-5 h-5 rounded border-oat-dark text-terracotta bg-paper focus:ring-2 focus:ring-terracotta/30 focus:ring-offset-0 cursor-pointer accent-terracotta"
    />
</template>
